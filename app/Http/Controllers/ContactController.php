    public function store(Request $request)
    {
        $this->validate($request, [
            'name' => ['nullable'],
            'email' => ['email', 'nullable'],
            'phone' => ['required'],
            'msg' => ['required'],
        ]);
        $contactCreated = Contact::create($request->all());
        if ($contactCreated) {
            return redirect()->back()->with('success', 'Thanks. We will back to you');
        }
    }

