    {
        $itineraries = collect($request->all());
        $itineraries = $itineraries->map(function ($itinerary) use ($id) {
            return  array_merge($itinerary, ['uuid' => Str::uuid(), 'package_id' => $id]);
        });
        DB::table('itineraries')->insert($itineraries->toArray());
    }

